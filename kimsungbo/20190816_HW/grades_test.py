from grades import grade


def test_grades():
    assert grade([{'국어': 100}], '국어') == ['A']
    assert grade([{'국어': 100}, {'국어': 80}], '국어') == ['A', 'B']
    assert grade([
        {'국어': 100, '영어': 90},
        {'국어': 80, '영어': 20}
    ], '영어') == ['A', 'F']

    assert grade([
        {'국어': 95, '영어': 90, '수학': 95},
        {'국어': 100, '영어': 20, '수학': 100},
        {'국어': 30, '영어': 10, '수학': 30},
    ], '수학') == ['A', 'A', 'D']
